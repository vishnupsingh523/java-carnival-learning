import java.util.Scanner;

public class substring{
    public static void main(String args[]){
        
        String s = "Vishwanath";

        // it will extracts the substring from the starting index 
        // to ending index. For example
        System.out.println("Strings Equal or not : "+s.substring(3, 7));
        System.out.println("Strings Equal or not : "+s.substring(3));



    }
}
